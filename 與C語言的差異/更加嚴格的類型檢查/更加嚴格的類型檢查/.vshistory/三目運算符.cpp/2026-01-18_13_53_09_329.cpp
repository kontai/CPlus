#define _CRT_SECURE_NO_WARNINGS
#include<iostream>

using namespace std;

/*
*2026/01/18
*AUTHOR:kontai
*
*本檔案演示三目運算符（條件運算符）在 C 和 C++ 中的差異：
*C 語言：返回值（右值），無法直接賦值
*C++ 語言：返回引用（左值），可以直接賦值
*/

int main() {
	int a = 10;
	int b = 20;

	// ========== C 語言方式 ==========
	// 三目運算符返回值的地址（指標）
	// 條件：a > b ? 是 : 否
	// 結果：a > b 為假，返回 &b（b 的地址）
	// *(取消引用)：將地址轉換為變數，並賦值 100
	// 執行後：b = 100
	*(a > b ? &a : &b) = 100;

	// ========== C++ 語言方式 ==========
	// C++ 中三目運算符可以返回左值引用
	// 條件：a > b ? 是 : 否
	// 結果：a > b 為假，返回對 b 的引用
	// 直接將引用所指向的變數賦值為 100
	// 執行後：b = 100
	(a > b ? a : b) = 100;

	// 輸出結果
	// 預期輸出：a = 10, b = 100
	cout << "a = " << a << ", b = " << b << endl;

	return EXIT_SUCCESS;
}